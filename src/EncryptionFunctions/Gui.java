    // Used to load image from file
    private static JLabel loadImage(String fileName) {
        BufferedImage img;
        JLabel imgContainer;
        try {
            img = ImageIO.read(new File(fileName));
            imgContainer = new JLabel(new ImageIcon(img));
            return imgContainer;
        } catch (Exception e) {
            e.printStackTrace();
            System.out.println("error loading image");
            return null;
        }
    }

